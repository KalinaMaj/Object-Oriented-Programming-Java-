/*Class ListNode creates nodes that stores data, and a reference to the next node proceeding it.  
 */

public class ListNode{
	
   public String data;
   public ListNode next;

   public ListNode(String d){
      data = d; //stored data is set equal to a particular string. The node now contains the String word called.
      next = null;
   } //constructor
  
}//class ListNode

